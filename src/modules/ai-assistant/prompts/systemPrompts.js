/**
 * 根据用户偏好调整 System Prompt
 */
export function adjustSystemPromptByPreferences(basePrompt, preferences) {
    let adjusted = basePrompt;

    // 输出风格偏好
    if (preferences.outputStyle === 'formal') {
        adjusted += '\n\n特别要求：输出应使用正式的书面语，避免口语化表达，注意用词的准确性和规范性。';
    } else if (preferences.outputStyle === 'casual') {
        adjusted += '\n\n特别要求：输出应自然流畅，可以适当使用口语化表达，让内容更亲切易读。';
    }

    return adjusted;
}
